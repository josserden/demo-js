/*
 * У змінній min лежить число від 0 до 59.Визначте, в яку чверть години потрапляє це число (у першу, другу, третю чи четверту).
 */

const min = 44;
let message;

if (min >= 0 && min < 15) {
  message = 'Це перша чверть';
}

if (min >= 15 && min < 30) {
  message = 'Це друга чверть';
}

if (min >= 30 && min < 45) {
  message = 'Це третя чверть';
}

if (min >= 45 && min <= 59) {
  message = 'Це четверта чверть';
}

console.log(message);
